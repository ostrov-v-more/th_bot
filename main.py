class Quiz(StatesGroup):
    waiting_for_disclaimer = State()  # Новое состояние для чтения предупреждений
    answering = State()


def get_keyboard(options):
    builder = ReplyKeyboardBuilder()
    for opt in options:
        builder.add(types.KeyboardButton(text=opt))
    return builder.as_markup(resize_keyboard=True)


dp = Dispatcher()


# Хендлер СТАРТА - теперь показывает первое предупреждение
@dp.message(Command("start"))
async def start_quiz(message: types.Message, state: FSMContext):
    # Сбрасываем данные и ставим индекс предупреждения на 0
    await state.update_data(
        disclaimer_index=0,
        current_question=0,
        total_score_1=0,
        total_score_2=0,
    )
    await state.set_state(Quiz.waiting_for_disclaimer)

    await message.answer(
        DISCLAIMERS[0],
        reply_markup=get_keyboard(["Продолжить ➡️"])
    )


# Хендлер перелистывания предупреждений (теперь ловит обе кнопки)
@dp.message(Quiz.waiting_for_disclaimer, F.text.in_(["Продолжить ➡️", "Начать 🚀"]))
async def next_disclaimer(message: types.Message, state: FSMContext):
    data = await state.get_data()
    current_idx = data['disclaimer_index']

    # Если нажали "Начать 🚀" на последнем слайде
    if message.text == "Начать 🚀":
        await state.set_state(Quiz.answering)
        q = QUESTIONS[0]
        # Не забываем экранировать точку в "1 из..." для MarkdownV2
        await message.answer(
            f"*1 из {len(QUESTIONS)}\.*: {q['text']}",
            reply_markup=get_keyboard(list(q['answers'].keys()))
        )
        return

    # Логика переключения слайдов по кнопке "Продолжить ➡️"
    next_idx = current_idx + 1
    await state.update_data(disclaimer_index=next_idx)

    if next_idx < len(DISCLAIMERS) - 1:
        # Показываем следующий слайд и кнопку "Продолжить"
        await message.answer(
            DISCLAIMERS[next_idx],
            reply_markup=get_keyboard(["Продолжить ➡️"])
        )
    else:
        # Показываем ПОСЛЕДНИЙ слайд и кнопку "Начать"
        await message.answer(
            DISCLAIMERS[next_idx],
            reply_markup=get_keyboard(["Начать 🚀"])
        )


# Хендлер ответов на вопросы (без изменений в логике)
@dp.message(Quiz.answering)
async def handle_answer(message: types.Message, state: FSMContext):
    data = await state.get_data()
    q_idx = data['current_question']
    current_q = QUESTIONS[q_idx]

    if message.text not in current_q['answers']:
        await message.answer("Пожалуйста, используйте кнопки\.")
        return

    # 1. Получаем баллы
    points = current_q['answers'][message.text]

    # 2. Определяем, какой счетчик обновить
    target = current_q.get("target", 1)  # По умолчанию 1, если забыли указать

    # 3. Обновляем данные в состоянии
    if target == 1:
        new_s1 = data['total_score_1'] + points
        await state.update_data(total_score_1=new_s1)
        total_score_1, total_score_2 = new_s1, data['total_score_2']
    else:
        new_s2 = data['total_score_2'] + points
        await state.update_data(total_score_2=new_s2)
        total_score_1, total_score_2 = data['total_score_1'], new_s2

    next_q_idx = q_idx + 1

    if next_q_idx < len(QUESTIONS):
        await state.update_data(current_question=next_q_idx)
        next_q = QUESTIONS[next_q_idx]
        await message.answer(
            f"*{next_q_idx + 1} из {len(QUESTIONS)}\.*: {next_q['text']}",
            reply_markup=get_keyboard(list(next_q['answers'].keys()))
        )
    else:
        # 4. Финальный результат с учетом двух счетчиков

        # Здесь вы можете прописать логику синдрома на основе двух чисел
        if total_score_1 <= 4 and total_score_2 <= 4:
            result = 1  # "Низкая вероятность"
        elif total_score_1 >= 8 and total_score_2 >= 8:
            result = 2  # "Смешанный"
        elif total_score_1 >= 8 and total_score_2 <= 7:
            result = 3  # "Гиперактивный"
        elif total_score_1 <= 7 and total_score_2 >= 8:
            result = 4  # "Невнимательный"
        elif (5 <= total_score_1 <= 7) or (5 <= total_score_2 <= 7):
            result = 5  # "Умеренное"
        else:
            result = 6  # "Синдром не выявлен*"

        messages = RESULT.get(result, ["Ошибка: результат не найден"])

        final_text = (
            f"*РЕЗУЛЬТАТЫ:*\n\n"
            f"*Гиперактивность / импульсивность:* {total_score_1}\n"
            f"*Дефицит внимания:* {total_score_2}\n"
            f"*Общий уровень:* {total_score_1 + total_score_2}"
        )

        await message.answer("📊 *Тест завершен\! Подсчитываем результаты\.\.\.*", reply_markup=types.ReplyKeyboardRemove())
        await asyncio.sleep(1)
        await message.answer(final_text)
        for text in messages:
            await message.answer(text)
            await asyncio.sleep(2)


        await state.clear()




async def main():
    bot = Bot(
        token=TOKEN,
        default=DefaultBotProperties(parse_mode=ParseMode.MARKDOWN_V2),
    )
    await dp.start_polling(bot)


if __name__ == "__main__":
    asyncio.run(main())
